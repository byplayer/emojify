// ReplaceEmoji replaces github style emoji to unicode emoji
func ReplaceEmoji(s string) string {
	emojiMap := GetEmojiMap()
	r := regexp.MustCompile(`:[a-zA-Z0-9_\-+]+:`)
	return r.ReplaceAllStringFunc(s, func(match_str string) string {
		emojiStr, exists := emojiMap[match_str]
		if exists {
			return emojiStr
		}
		return match_str
	})
}
